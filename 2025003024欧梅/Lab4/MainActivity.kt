@Composable
fun DiceRollerApp() {

    Column(
        modifier = Modifier.fillMaxSize(),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        DiceWithButtonAndImage()
    }
}

@Composable
fun DiceWithButtonAndImage(modifier: Modifier = Modifier) {
    var result = remember { mutableIntStateOf(1) }

    val imageResource = when (result.intValue) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }

    Column(
        modifier = modifier,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Image(
            painter = painterResource(imageResource),
            contentDescription = result.intValue.toString()
        )

        Spacer(modifier = Modifier.height(24.dp))

        Button(
            onClick = { result.intValue = (1..6).random() },
        ) {
            Text(
                text = stringResource(R.string.roll),
                style = MaterialTheme.typography.labelLarge //按钮文字样式
            )
        }
    }
}

